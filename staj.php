			    <div class="style2">
				<ul>
				    <li>Öğrenci toplam 50 iş günü staj yapmak zorundadır.</li>
					<ul>
						<li>50 iş gününün tamamı aynı işyerinde veya işyerinin aynı biriminde yapılamaz.</li>
						<li>50 iş günlük stajın ilk 25 günü 2. Sınıf bittikten sonra yapılabilir. 1.  Sınıf bittikten sonra staj yapılsa dahi kabul edilmemektedir. </li>
				        <li>2. sınıfın bitiminde 50 iş günü stajın tamamı yapılamaz. Stajın bir kısmı  3. Sınıf bittikten sonra yapabilir. </li>
				        <li>50 iş günlük staj için sigorta üniversite tarafından yapılmaktadır. Staj  yapılan kurumun sigorta ödeme zorunluluğu yoktur. Eğer staj yapılan kurum  stajyerin sigortasını ödüyorsa, tekrar üniversite tarafından sigorta yapılmasına  gerek yoktur. Bunun için staj komisyonuna bilgi verilmesi gerekmektedir. </li>
				        <li>II. öğretimler öğretim dönemi içerisinde mesai saatlerinde staj yapabilirler.  I. Öğretimler öğretim dönemi içerisinde mesai saatleri içerisinde ders devam zorunluluğundan  dolayı staj yapamazlar. II öğretimlerin de yine I. Öğretimler gibi 2. Sınıf  öğretim yılı bitmeden staj yapmak hakları yoktur. Öğrencilerin staj hakkı 2.  Sınıf bittikten sonra başlar. </li>
				        <li>Derse devam zorunluluğu kalmayan I.öğretim öğrenciler dönem içerisinde staj  yapabilirler.<a name="_GoBack"></a></li>
				        <li>50 iş günü staj 2 veya daha fazla kurumda şu şekilde yapılabilir.</li>
				        <ul>
				           <li>25 +25 iş günü</li>
				           <li>20+30 iş günü</li>
				           <li>15+35 iş günü</li>
				           <li>20+20+10 iş günü</li>
			            </ul>
					</ul>
			        <li>Staj yeri bulma sorumluluğu öğrenciye aittir.</li>
				    <li>Staj yapılan kurumda ilgili meslek ile ilgili mühendis(Bilgisayar,  Elektronik, Elektrik, Mekatronik, Yazılım Müh.) olması zorunludur. Stajyerden  sorumlu mühendisin mesleki iş alanı Bilgisayar mühendisliği İle ilgili  olmalıdır. Yazılım Şirketleri, Üretim (Fabrika gibi) yerlerinin Bilgi İşlem  merkezleri, Haberleşme Teknolojisi Firmalarının yazılım ve bilgisayar donanım  merkezleri, Bilişim Ar-ge merkezleri veya firmaları, Devlet  kurumlarının(Bakanlıkların, Büyükşehir Belediyelerin, Genel Müdürlüklerin veya  üniversitelerin bilgi işlem merkezleri, bankaların genel bilgi işlem  merkezleri(şubelerin değil) Bilgisayar mühendisliği için genellikle staj yapma  alanlarıdır.   </li>
				    <li>Staj, mühendislik anlamında teorik bilgi öğrenme amaçlı değil, teorik  bilginin uygulamasının görülmesi geliştirilmesi ve değerlendirilmesi amacı ile  yapılmaktadır. Bu bakımdan esas amaç staj yerindeki Bilgisayar mühendisliği  mesleğinin görev ve sorumluluklarını, işleyişini görmek ve sistem bakımından  gözlem yapmaktır. </li>
			      </ul>
