					<li><b>2)</b> Öğrencinin formu 2 nüsha halinde doldurup (Sırası Ek-2’de gösterilmiştir!) staj komisyonuna staja başlamadan en geç 1 ay kala teslim etmesi gerekmektedir. </li>
					<li><b>3)</b> Formun bir nüshası komisyon tarafından onaylandıktan sonra size teslim edilecektir. Form teslim edildikten sonra aşağıdaki "STAJ LİNKİ" ne tıklayıp bilgilerinizi girmeniz gerekmektedir. Bu konudaki sorumluluk size aittir. Staj belgelerini komisyona aşağıda belirtilen gün ve saatlerde getirmeniz gerekmektedir. Belirtilen zaman dışında getirilen belgeler alınmayacaktır. Staj ile ilgili sorularınızı sadece aşağıda belirtilen saatlerde sorabilirsiniz.<li><br>
