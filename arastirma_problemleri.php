				<p><br><span class="style1">Araştırma Problemleri / Bitirme Tezi Dersi  Seçiminde İzlenecek Adımlar:</span><br>
				<span class="style1">1) </span> İlgili  dönem itibariyle Araştırma Problemleri / Bitirme Projesi Derslerini verecek  hocaların kontenjanları eşit sayıda olacak şekilde belirlenir.<br><br>
				</p>
				<p><span class="style1">2) </span>Bitirme komisyonu tarafından hocalara ait  kontenjan sayılarını ve çalışma konularını içeren bir duyuru yayınlanır. Proje  konuları en az iki kişilik öğrenci gruplarına uygun olacak şekilde belirlenir.<br><br>
				</p>
				<p><span class="style1">3) </span>Öğrenciler Araştırma Problemleri / Bitirme  Projesi Dersleri ile ilgili akademik takvimde yer alan ekle/çıkar haftasından  önce çalışmayı düşündükleri hocalar ile iletişime geçerler.<br><br>
                </p>
				<p><span class="style1">4) </span>Ders ekle/çıkar haftası içerisinde herhangi bir  hoca ile anlaşmamış olan öğrenciler danışmanları tarafından boşta kontenjanı  olan hocalara atanır.<br><br>
                </p>
				<p><span class="style1">5) </span>Derslerin başlangıcı ile birlikte hoca tarafından atılan duyuru ile birlikte yapılan toplantıda herhangi bir proje grubuna dâhil olmayan öğrenciler, hoca tarafından bir proje grubuna atanır. (Projelerin en az iki kişilik gruplar halinde yapılması zorunludur.)<br><br>
				</p>
				<p><span class="style1">6) </span>2016-2017 Güz dönemi itibariyle ders planında 7. Dönem Projesi olarak yer alan dersin ismi, Araştırma Problemleri olarak değiştirilmiştir.<br><br>
				</p>
				<p><span class="style1">7) </span> Araştırma Problemleri dersinde daha önce verilen 7. Dönem Projesi dersinden farklı olarak, öğrencilerden sekizinci dönemde verilen Bitirme Çalışması dersinin projesi için gerekli ön çalışmaların yapılması beklenmektedir.<br>
				</p><br><br><br>
				<p><span class="style1">Araştırma Problemleri ya da  Bitirme Tezi Tesliminde İzlenecek Yollar:</span></p>
				<p><span class="style1">1) </span> <b>EK-1</b>'deki Tez/Proje Şablonunu indiriniz. Çalışmalarınızı bu şablon üzerinden yapmanız size kolaylık 
			      sağlayacaktır. Şablon detaylarını görebilmek için Word'bayrak işaretçisini' aktif hale getiriniz. Şablonda problem yaşamanız durumunda, <b>EK-2</b> ve <b>EK-3</b>'teki gerekli açıklamaları okuyunuz.<br><br>
		        </p>
				 <p><span class="style1">2)</span> Ciltlettirme aşamasından önce çıktı alıp danışman hocanıza gösteriniz. Bu aşama, danışman hocanızın isteği doğrultusunda 
			      farklılık gösterebilir. Dikkat edilmesi gereken bir nokta; Proje  ya da tezinizi   <b>PDF</b> formatında kaydederek çıktı alınız.
			      Aksi durumda sayfanızda kaymalar olabilecektir.<br><br>
		        </p>
			    <p><span class="style1">3) </span><b>EK-4</b>'teki sizin için uygun olan Jüri Ortak Raporunu bilgisayar ortamında doldurunuz. Size ait olan bölümleri doldurunuz.
			      Bu sayfa, ciltleme aşamasında proje ya da tezin içerinde yer alan ÖNSÖZ ve TEŞEKKÜR bölümünden önce olacak şekilde projeye/teze eklenecektir.<br><br>
		        </p>
			    <p><span class="style1">4)</span> <b>EK-5</b>'te proje/tez kapak ve sırt bilgileri bulunmaktadır. Ciltleleme aşamasında bu bilgileri de projeye ya da teze ekleyiniz/ekletiniz. 
			      Bu bilgiler proje/tez içeriğine göre ve proje/tez yürütücüsüne göre düzenlenmelidir.<br><br>
		        </p>
			    <p><b>Not</b>: Dönem projeleri 1 tane beyaz karton kapaklı olacaktır. Bitirme tezleri ise 1 tane mavi bez ciltli olacaktır.</p><br>
				
				
